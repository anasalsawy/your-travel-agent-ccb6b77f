-- Create storage bucket for payment proofs (private)
INSERT INTO storage.buckets (id, name, public)
VALUES ('proof-uploads', 'proof-uploads', false)
ON CONFLICT (id) DO NOTHING;

-- Storage policies for proof-uploads bucket
-- Users can upload their own proofs
CREATE POLICY "Users can upload payment proofs"
ON storage.objects
FOR INSERT
TO authenticated
WITH CHECK (
  bucket_id = 'proof-uploads' 
  AND (storage.foldername(name))[1] = auth.uid()::text
);

-- Users can view their own proofs
CREATE POLICY "Users can view own payment proofs"
ON storage.objects
FOR SELECT
TO authenticated
USING (
  bucket_id = 'proof-uploads' 
  AND (storage.foldername(name))[1] = auth.uid()::text
);

-- Admins can view all proofs
CREATE POLICY "Admins can view all payment proofs"
ON storage.objects
FOR SELECT
TO authenticated
USING (
  bucket_id = 'proof-uploads' 
  AND has_role(auth.uid(), 'admin'::app_role)
);

